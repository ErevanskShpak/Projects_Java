package SomePackage;

public class Cart {
    private String name;
    private String surname;
    private int price;

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public String getSurname() {
        return surname;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
